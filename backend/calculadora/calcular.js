class Calcular {
    sumar(num1, num2) {
        return num1 + num2;
    }

    restar(num1, num2) {
        return num1 - num2;
    }

    dividir(num1, num2) {
        return num1 / num2;
    }

    multiplicar(num1, num2) {
        return num1 * num2;
    }
    potencia(num1, num2){
        return Math.pow(num1, num2);
    }
    raiz(num2){
        return Math.sqrt(num2);
    }
    resto(num1, num2){
        return num1 % num2;
    }
} 